#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t i;
	size_t count_src;

	i = 0;
	count_src = 0;
	if (!dst || !src)
		return (0);
	while (src[count_src])
		count_src++;
	if (dstsize > 0)
	{
		while (src[i] && i < dstsize - 1)
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	return (count_src);
}
